#Executar para ver resultados#

#Convertendo de float para inteiro#
print(int(1.9))

#Convertendo de string para int#
print(int("10"))

#Convertendo de str para float#
print(float("10.10"))

#Convertendo de int para float#
print(float(100))

#Também é possível inverter e converter de números para string#
print(type(str(10.19)))

#Ao dividir um número inteiro o python faz o resultado sempre ser float, mas podemos usar o operador // para transformá-lo em int#
print(100 / 2)
print(100 // 2)