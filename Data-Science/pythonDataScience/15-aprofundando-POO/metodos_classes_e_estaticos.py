#métodos de classe 
#estão ligados a classe e não ao objeto
#aponta para a classe e não para a instancia do objeto


#métodos estáticos
#não recebe um primeiro argumento explicito
#também é vinculado a classe e não ao objeto da classe
#ele está presente em uma classe porque faz sentido que o metodo esteja na classe


#um métoo de classe pode acessar ou modificar o estado da classe
#já o metodo estático não pode acessá-lo ou modificá-lo


#quando utilizar cada método
#o método classe geralmente usa para criar métodos de fábrica
#ja o metodo estatico é usado para criar funções utilitárias